# 스타일 헬퍼
def set_font(run, bold=False, size=11, color=None):
    run.bold = bold
    run.font.size = Pt(size)
    if color:
        run.font.color.rgb = RGBColor(*color)

def add_title(doc, text):
    p = doc.add_heading(level=0)
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.runs[0] if p.runs else p.add_run(text)
    run.text = text
    set_font(run, bold=True, size=20, color=(0x1f, 0x6f, 0xeb))
    return p

def add_subtitle(doc, text):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(text)
    set_font(run, size=10, color=(0x8b, 0x94, 0x9e))

def add_category_heading(doc, text):
    p = doc.add_heading(level=1)
    run = p.runs[0] if p.runs else p.add_run(text)
    run.text = text
    set_font(run, bold=True, size=14, color=(0x58, 0xa6, 0xff))

def set_cell_bg(cell, hex_color):
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear')
    shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'), hex_color)
    tcPr.append(shd)

def add_table(doc, rows_data):
    # 컬럼: #, 아이디어, 해결하는 문제, 도구
    table = doc.add_table(rows=1, cols=4)
    table.style = 'Table Grid'

    # 헤더 행
    hdr = table.rows[0].cells
    headers = ['#', '아이디어', '해결하는 문제', '도구']
    widths = [Cm(1.0), Cm(9.0), Cm(8.5), Cm(3.0)]
    for i, (cell, htext) in enumerate(zip(hdr, headers)):
        cell.width = widths[i]
        set_cell_bg(cell, '1f2937')
        p = cell.paragraphs[0]
        run = p.add_run(htext)
        set_font(run, bold=True, size=10, color=(0xe1, 0xe4, 0xe8))
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER

    for row_data in rows_data:
        row = table.add_row()
        cells = row.cells
        for i, (cell, val) in enumerate(zip(cells, row_data)):
            cell.width = widths[i]
            p = cell.paragraphs[0]
            # 도구 컬럼 색상
            if i == 3:
                if 'Secretary' in val and 'OpenClaw' in val:
                    color = (0xf0, 0x88, 0x3e)
                elif 'Secretary' in val:
                    color = (0x79, 0xc0, 0xff)
                elif 'OpenClaw' in val:
                    color = (0x56, 0xd3, 0x64)
                else:
                    color = (0xe1, 0xe4, 0xe8)
                run = p.add_run(val)
                set_font(run, bold=True, size=9, color=color)
                p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            elif i == 0:
                run = p.add_run(val)
                set_font(run, size=9, color=(0x8b, 0x94, 0x9e))
                p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            elif i == 1:
                # 굵은 제목 + 일반 설명
                parts = val.split(' — ', 1)
                r1 = p.add_run(parts[0])
                set_font(r1, bold=True, size=9, color=(0xe1, 0xe4, 0xe8))
                if len(parts) > 1:
                    r2 = p.add_run(' — ' + parts[1])
                    set_font(r2, size=9, color=(0x8b, 0x94, 0x9e))
            else:
                run = p.add_run(val)
                set_font(run, size=9, color=(0x8b, 0x94, 0x9e))

    doc.add_paragraph()

