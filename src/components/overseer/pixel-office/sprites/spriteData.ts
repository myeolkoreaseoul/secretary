// Build sprite sets with simplified directions (mirror left/right)
function buildSpriteSet(
  idle: SpriteFrame,
  walk: SpriteFrame,
  typing: SpriteFrame,
) {
  return {
    idle: { down: [idle], up: [idle], left: [idle], right: [idle] },
    walking: { down: [idle, walk], up: [idle, walk], left: [idle, walk], right: [idle, walk] },
    typing: [typing, idle],
    offline: [idle],
  };
}

